import cookie from 'js-cookie'
import userApi from '@/api/login'

export default {
  data() {
    return {
      token: '',
      loginInfo: {
        id: '',
        age: '',
        avatar: '',
        mobile: '',
        nickname: '',
        sex: ''
      },
      avatar: ''
    }
  },

  created () {
    // 判断是否是微信登录
    this.token = this.$route.query.token
    // token存在
    if(this.token){
      this.wxlogin()
    }else{
      this.showUserInfo()
    }
  },

  methods: {
    showUserInfo() {
      //从cookie中获取用户信息
      var user = cookie.get('user_info')
      if(user){
        this.loginInfo = JSON.parse(user)
      }
    },

    logout() {
      cookie.set('user_info', "", {domain: 'localhost'})
      cookie.set('user_token', "", {domain: 'localhost'})

      //跳转页面
      window.location.href = '/'
    },

    wxlogin() {
      if(this.token == ''){
        return
      }else{
        //设置cookie
        cookie.set('user_token', this.token, { domain: 'localhost' })
        cookie.set('user_info', '', { domain: 'localhost' })

        //发送请求获取登录信息
        userApi.getLoginInfo()
          .then(response => {
            this.loginInfo = response.data.data.item
            console.log(this.loginInfo)
            cookie.set('user_info', this.loginInfo, { domain: 'localhost' })
          })
      }
    }
  },
};
